// Q10. In Range 10–50
// Given a number, check if it lies between 10 and 50 (inclusive).

let num = Number(prompt("Enter a num: "));

if(num >= 10 && num <= 50) console.log("Num is in range.");
else console.log("Num isn't in range.");

